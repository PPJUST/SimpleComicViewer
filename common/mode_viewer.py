# 显示模式，单页/双页/卷轴

class ModeViewer:
    """ 显示模式，单页/双页/卷轴"""

    class SinglePage:
        """单页"""

    class DoublePage:
        """双页"""

        class Left:
            """左开本"""

        class Right:
            """右开本"""

    class Scroll:
        """卷轴"""

        class Horizontal:
            """横向"""

            class Left:
                """左开本"""

            class Right:
                """右开本"""

        class Vertical:
            """纵向"""
